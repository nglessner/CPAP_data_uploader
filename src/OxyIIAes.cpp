size_t encryptEcbPkcs7(const uint8_t* plaintext, size_t plaintextLen,
                       const uint8_t key[KEY_SIZE],
                       uint8_t* out, size_t outCap) {
    if (!key || !out) return 0;
    // Pad in place into out[]
    size_t padded = pkcs7Pad(plaintext, plaintextLen, out, outCap);
    if (padded == 0) return 0;

#ifdef UNIT_TEST
    uint8_t roundKeys[EXPANDED_KEY_BYTES];
    keyExpansion(key, roundKeys);
    for (size_t off = 0; off < padded; off += BLOCK_SIZE) {
        aesEncryptBlock(out + off, roundKeys, out + off);
    }
#else
    mbedtls_aes_context ctx;
    mbedtls_aes_init(&ctx);
    if (mbedtls_aes_setkey_enc(&ctx, key, 128) != 0) {
        mbedtls_aes_free(&ctx);
        return 0;
    }
    for (size_t off = 0; off < padded; off += BLOCK_SIZE) {
        if (mbedtls_aes_crypt_ecb(&ctx, MBEDTLS_AES_ENCRYPT, out + off, out + off) != 0) {
            mbedtls_aes_free(&ctx);
            return 0;
        }
    }
    mbedtls_aes_free(&ctx);
#endif

    return padded;
}

size_t decryptEcbPkcs7(const uint8_t* ciphertext, size_t ciphertextLen,
                       const uint8_t key[KEY_SIZE],
                       uint8_t* out, size_t outCap) {
    if (!key || !ciphertext || !out) return 0;
    if (ciphertextLen == 0 || ciphertextLen % BLOCK_SIZE != 0) return 0;
    if (outCap < ciphertextLen) return 0;

#ifdef UNIT_TEST
    uint8_t roundKeys[EXPANDED_KEY_BYTES];
    keyExpansion(key, roundKeys);
    for (size_t off = 0; off < ciphertextLen; off += BLOCK_SIZE) {
        aesDecryptBlock(ciphertext + off, roundKeys, out + off);
    }
#else
    mbedtls_aes_context ctx;
    mbedtls_aes_init(&ctx);
    if (mbedtls_aes_setkey_dec(&ctx, key, 128) != 0) {
        mbedtls_aes_free(&ctx);
        return 0;
    }
    for (size_t off = 0; off < ciphertextLen; off += BLOCK_SIZE) {
        if (mbedtls_aes_crypt_ecb(&ctx, MBEDTLS_AES_DECRYPT, ciphertext + off, out + off) != 0) {
            mbedtls_aes_free(&ctx);
            return 0;
        }
    }
    mbedtls_aes_free(&ctx);
#endif

    size_t unpadded = pkcs7Unpad(out, ciphertextLen);
    if (unpadded == 0) return 0;
    return unpadded;
}

