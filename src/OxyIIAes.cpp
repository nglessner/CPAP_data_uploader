// PKCS7 pad: append (BLOCK_SIZE - len%BLOCK_SIZE) bytes, each equal to that
// count. Always adds at least one byte, even when the input is block-aligned.
size_t pkcs7Pad(const uint8_t* in, size_t inLen, uint8_t* out, size_t outCap) {
    using OxyIIAes::BLOCK_SIZE;
    uint8_t pad = static_cast<uint8_t>(BLOCK_SIZE - (inLen % BLOCK_SIZE));
    size_t total = inLen + pad;
    if (outCap < total) return 0;
    if (in && inLen) memcpy(out, in, inLen);
    for (size_t i = inLen; i < total; i++) out[i] = pad;
    return total;
}

// PKCS7 unpad: returns the unpadded length, or 0 if the trailing padding is
// invalid (last byte 0 or > 16, or the trailing run isn't constant).
size_t pkcs7Unpad(const uint8_t* in, size_t inLen) {
    using OxyIIAes::BLOCK_SIZE;
    if (inLen == 0 || inLen % BLOCK_SIZE != 0) return 0;
    uint8_t pad = in[inLen - 1];
    if (pad == 0 || pad > BLOCK_SIZE) return 0;
    if (pad > inLen) return 0;
    for (size_t i = inLen - pad; i < inLen; i++) {
        if (in[i] != pad) return 0;
    }
    return inLen - pad;
}

