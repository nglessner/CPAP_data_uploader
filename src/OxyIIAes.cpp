const uint8_t kRcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36
};

void keyExpansion(const uint8_t* key, uint8_t* roundKeys) {
    uint8_t tmp[4];
    memcpy(roundKeys, key, Nk * 4);

    for (size_t i = Nk; i < Nb * (Nr + 1); i++) {
        memcpy(tmp, roundKeys + (i - 1) * 4, 4);
        if (i % Nk == 0) {
            // RotWord
            uint8_t t = tmp[0]; tmp[0] = tmp[1]; tmp[1] = tmp[2]; tmp[2] = tmp[3]; tmp[3] = t;
            // SubWord
            for (int j = 0; j < 4; j++) tmp[j] = kSbox[tmp[j]];
            // Rcon
            tmp[0] ^= kRcon[i / Nk];
        }
        for (int j = 0; j < 4; j++) {
            roundKeys[i * 4 + j] = roundKeys[(i - Nk) * 4 + j] ^ tmp[j];
        }
    }
}

void addRoundKey(uint8_t state[16], const uint8_t* roundKey) {
    for (int i = 0; i < 16; i++) state[i] ^= roundKey[i];
}

void subBytes(uint8_t state[16])    { for (int i = 0; i < 16; i++) state[i] = kSbox[state[i]]; }
void invSubBytes(uint8_t state[16]) { for (int i = 0; i < 16; i++) state[i] = kRsbox[state[i]]; }

void shiftRows(uint8_t s[16]) {
    uint8_t t;
    // row 1
    t = s[1]; s[1] = s[5]; s[5] = s[9]; s[9] = s[13]; s[13] = t;
    // row 2
    t = s[2]; s[2] = s[10]; s[10] = t;
    t = s[6]; s[6] = s[14]; s[14] = t;
    // row 3
    t = s[15]; s[15] = s[11]; s[11] = s[7]; s[7] = s[3]; s[3] = t;
}

void invShiftRows(uint8_t s[16]) {
    uint8_t t;
    // row 1
    t = s[13]; s[13] = s[9]; s[9] = s[5]; s[5] = s[1]; s[1] = t;
    // row 2
    t = s[2]; s[2] = s[10]; s[10] = t;
    t = s[6]; s[6] = s[14]; s[14] = t;
    // row 3
    t = s[3]; s[3] = s[7]; s[7] = s[11]; s[11] = s[15]; s[15] = t;
}

uint8_t xtime(uint8_t x) { return static_cast<uint8_t>((x << 1) ^ ((x >> 7) ? 0x1b : 0x00)); }

void mixColumns(uint8_t s[16]) {
    for (int c = 0; c < 4; c++) {
        uint8_t a0 = s[c*4 + 0], a1 = s[c*4 + 1], a2 = s[c*4 + 2], a3 = s[c*4 + 3];
        uint8_t t = a0 ^ a1 ^ a2 ^ a3;
        s[c*4 + 0] ^= t ^ xtime(a0 ^ a1);
        s[c*4 + 1] ^= t ^ xtime(a1 ^ a2);
        s[c*4 + 2] ^= t ^ xtime(a2 ^ a3);
        s[c*4 + 3] ^= t ^ xtime(a3 ^ a0);
    }
}

uint8_t gmul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for (int i = 0; i < 8; i++) {
        if (b & 1) p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if (hi) a ^= 0x1b;
        b >>= 1;
    }
    return p;
}

void invMixColumns(uint8_t s[16]) {
    for (int c = 0; c < 4; c++) {
        uint8_t a0 = s[c*4 + 0], a1 = s[c*4 + 1], a2 = s[c*4 + 2], a3 = s[c*4 + 3];
        s[c*4 + 0] = gmul(a0, 0x0e) ^ gmul(a1, 0x0b) ^ gmul(a2, 0x0d) ^ gmul(a3, 0x09);
        s[c*4 + 1] = gmul(a0, 0x09) ^ gmul(a1, 0x0e) ^ gmul(a2, 0x0b) ^ gmul(a3, 0x0d);
        s[c*4 + 2] = gmul(a0, 0x0d) ^ gmul(a1, 0x09) ^ gmul(a2, 0x0e) ^ gmul(a3, 0x0b);
        s[c*4 + 3] = gmul(a0, 0x0b) ^ gmul(a1, 0x0d) ^ gmul(a2, 0x09) ^ gmul(a3, 0x0e);
    }
}

void aesEncryptBlock(const uint8_t in[16], const uint8_t* roundKeys, uint8_t out[16]) {
    uint8_t state[16];
    memcpy(state, in, 16);
    addRoundKey(state, roundKeys);
    for (size_t round = 1; round < Nr; round++) {
        subBytes(state);
        shiftRows(state);
        mixColumns(state);
        addRoundKey(state, roundKeys + round * 16);
    }
    subBytes(state);
    shiftRows(state);
    addRoundKey(state, roundKeys + Nr * 16);
    memcpy(out, state, 16);
}

void aesDecryptBlock(const uint8_t in[16], const uint8_t* roundKeys, uint8_t out[16]) {
    uint8_t state[16];
    memcpy(state, in, 16);
    addRoundKey(state, roundKeys + Nr * 16);
    for (size_t round = Nr - 1; round > 0; round--) {
        invShiftRows(state);
        invSubBytes(state);
        addRoundKey(state, roundKeys + round * 16);
        invMixColumns(state);
    }
    invShiftRows(state);
    invSubBytes(state);
    addRoundKey(state, roundKeys);
    memcpy(out, state, 16);
}
